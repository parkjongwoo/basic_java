package com.day02.operator;

public class Ex09_Assignment {

	public static void main(String[] args) {
		
		int a=10,b=7;
		a+=b;
		System.out.println(a);//17
		a-=b;
		System.out.println(a);//10
		a*=b;
		System.out.println(a);//70
		a/=b;
		System.out.println(a);//10
		a%=b;
		System.out.println(a);//3
		
		
	}

}
